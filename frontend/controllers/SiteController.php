    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['logout', 'signup'],
                'rules' => [
                    [
                        'actions' => ['signup'],
                        'allow' => true,
                        'roles' => ['?'],
                    ],
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return mixed
     */
    public function actionIndex()
    {
        $slides = Slider::find()->asArray()->all();

        $category = Categories::find()->with('products')->where(['id' => 16])->asArray()->one();

        $products = Products::find()->where(['is_hit' => '1']);
        $t_products = Products::find()->where(['is_sale' => '1']);

        $pages = new Pagination(['totalCount' => $products->count(), 'pageSize' =>4]);
        $pages = new Pagination(['totalCount' => $t_products->count(), 'pageSize' =>4]);

        $products = $products->offset($pages->offset)->limit($pages->limit)->asArray()->all();
        $t_products = $t_products->offset($pages->offset)->limit($pages->limit)->asArray()->all();
//        $hot_products = Products::find()->where(['is_hit' => 1])->asArray()->all();
//        var_dump($t_products); die();


            return $this->render('home',
                [
                    'category' => '$category',
                    'slider'=>$slides,
                    'hot_products' => $products,
                    'top_products' => $t_products,
                    'pager' => $pages
                ]);


    }

    /**
     * Logs in a user.
     *
     * @return mixed
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        } else {
            $model->password = '';

            return $this->render('login', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Logs out the current user.
     *
     * @return mixed
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return mixed
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->validate()) {
            if ($model->sendEmail(Yii::$app->params['adminEmail'])) {
                Yii::$app->session->setFlash('success', 'Thank you for contacting us. We will respond to you as soon as possible.');
            } else {
                Yii::$app->session->setFlash('error', 'There was an error sending your message.');
            }

            return $this->refresh();
        } else {
            return $this->render('contact', [
                'model' => $model,
            ]);
        }
    }

    public function actionAbout(){

        $this->layout = 'about';

        $article = Articles::find()->where(['slug'=>'about'])->asArray()->one();
        return $this->render('about',[
            'article' => $article
        ]);
    }

    public function actionPrivacy(){

        return $this->render('privacy',[]);
    }

